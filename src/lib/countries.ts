export function getStoredCountry(): CountryChoice | null {
  try {
    const raw = localStorage.getItem(COUNTRY_KEY);
    if (!raw) return null;
    const parsed = JSON.parse(raw) as CountryChoice;
    if (parsed && parsed.code && parsed.name) return parsed;
    return null;
  } catch {
    return null;
  }
}

export function setStoredCountry(country: CountryChoice): void {
  try {
    localStorage.setItem(COUNTRY_KEY, JSON.stringify(country));
  } catch {
    // ignore
  }
}

